package br.com.github.kaueopg.sac.model;

public class Master{

    private String nomeMaster = "Master";
    private String cpfMaster = "999.999.999-99";
    private String senhaMaster = "SenhaMaster";

    public String getNomeMaster() {
        return nomeMaster;
    }
    public void setNomeMaster(String nomeMaster) {
        this.nomeMaster = nomeMaster;
    }
    public String getCpfMaster() {
        return cpfMaster;
    }
    public void setCpfMaster(String cpfMaster) {
        this.cpfMaster = cpfMaster;
    }
    public String getSenhaMaster() {
        return senhaMaster;
    }
    public void setSenhaMaster(String senhaMaster) {
        this.senhaMaster = senhaMaster;
    }
    
}
